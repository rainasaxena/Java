public class Operators {
    public static void main(String[] args) {
        int a=2;
        System.out.println(a++);
        System.out.println(++a);

        int b=a+3;
        int c=3;
        c*=2;

        System.out.println(b);

        System.out.println(a>b);

        System.out.println(a!=b);

        System.out.println(c);

        System.out.println(64>3 && 64<6);
        System.out.println(64>3 && 64<6);
    }
    
}
