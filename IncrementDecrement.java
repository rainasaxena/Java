public class IncrementDecrement {
    public static void main(String[] args) {
        int i=56;
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i++);

        int j=5;
        System.out.println(j);
        System.out.println(--j); //decrease and execute
        System.out.println(j--); //execute and decrease
    }
}
